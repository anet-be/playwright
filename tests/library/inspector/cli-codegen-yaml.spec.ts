import { test, expect } from './inspectorTest';

test('should print the correct YAML header', async ({ runCLI, server }) => {
  const cli = runCLI(['--target=yaml', server.EMPTY_PAGE]);
  await cli.waitFor(`# Generated by Genfest`);
  await cli.waitFor(`version:`);
  await cli.waitFor(`name:`);
  await cli.waitFor(`baseURL:`);
  await cli.waitFor(`steps:`);
});

test('should record navigation', async ({ runCLI, server }) => {
  const cli = runCLI(['--target=yaml', server.EMPTY_PAGE]);
  await cli.waitFor(`- action: 'navigate'`);
  await cli.waitFor(`url: '${server.EMPTY_PAGE}'`);
});

test('should skip about:blank navigation', async ({ runCLI }) => {
  const cli = runCLI(['--target=yaml', 'about:blank']);

  // Should have header but no navigate step for about:blank
  await cli.waitFor(`# Generated by Genfest`);

  const output = await cli.text();
  expect(output).not.toContain('about:blank');
});
